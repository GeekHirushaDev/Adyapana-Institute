    private void markAttendance(String status) {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to mark attendance.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int sno = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
        String classname = jComboBox2.getSelectedItem().toString();
        String date = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate());

        if (date == null || date.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a date.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            String query = "SELECT classno FROM classes WHERE classname = '" + classname + "'";
            ResultSet rs = MySQL.executeSearch(query);
            if (rs.next()) {
                int classno = rs.getInt("classno");

                String insertQuery = "INSERT INTO attendance (sno, classno, date, status) VALUES (" + sno + ", " + classno + ", '" + date + "', '" + status + "')";
                int rowsAffected = MySQL.executeIUD(insertQuery);
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Attendance marked as " + status + ".", "Success", JOptionPane.INFORMATION_MESSAGE);
                    loadAttendanceReport();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error marking attendance: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

