    private void loadClassesIntoComboBox() {
        jComboBox2.removeAllItems();
        jComboBox2.addItem("Select Class");
        jComboBox2.setSelectedItem("Select Class");

        jComboBox3.removeAllItems();
        jComboBox3.addItem("All Classes");
        jComboBox3.setSelectedItem("All Classes");

        try {
            ResultSet rs = MySQL.executeSearch("SELECT classname FROM classes");
            while (rs.next()) {
                jComboBox2.addItem(rs.getString("classname"));
                jComboBox3.addItem(rs.getString("classname"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading classes: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadAttendanceReport() {
        if (jComboBox3.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "No class selected.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String selectedClass = jComboBox3.getSelectedItem().toString();
        String query;

        if (selectedClass.equals("All Classes")) {
            query = "SELECT attendance.date, classes.classname, students.name, attendance.status "
                    + "FROM attendance "
                    + "INNER JOIN classes ON attendance.classno = classes.classno "
                    + "INNER JOIN students ON attendance.sno = students.sno";
        } else {
            query = "SELECT attendance.date, classes.classname, students.name, attendance.status "
                    + "FROM attendance "
                    + "INNER JOIN classes ON attendance.classno = classes.classno "
                    + "INNER JOIN students ON attendance.sno = students.sno "
                    + "WHERE classes.classname = '" + selectedClass + "'";
        }

        try {
            ResultSet rs = MySQL.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("date"));
                row.add(rs.getString("classname"));
                row.add(rs.getString("name"));
                row.add(rs.getString("status"));
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading attendance report: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadStudentsForClass(String classname) {
        try {
            String query = "SELECT students.sno, students.name, students.gender "
                    + "FROM students "
                    + "INNER JOIN enrollments ON students.sno = enrollments.sno "
                    + "INNER JOIN classes ON enrollments.classno = classes.classno "
                    + "WHERE classes.classname = '" + classname + "'";
            ResultSet rs = MySQL.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Vector<String> row = new Vector<>();
                row.add(rs.getString("sno"));
                row.add(rs.getString("name"));
                row.add(rs.getString("gender"));
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading students: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

