    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        try {
            String searchText = jTextField5.getText().trim();
            String query = "SELECT p.paymentid, s.name AS student, p.amount, p.date, i.invoiceno "
                    + "FROM payments p "
                    + "JOIN students s ON p.sno = s.sno "
                    + "JOIN invoices i ON p.invoiceno = i.invoiceno "
                    + "WHERE s.name LIKE '%" + searchText + "%' OR p.paymentid LIKE '%" + searchText + "%'";

            ResultSet resultSet = MySQL.executeSearch(query);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                model.addRow(new Object[]{
                    resultSet.getInt("paymentid"),
                    resultSet.getString("student"),
                    resultSet.getDouble("amount"),
                    resultSet.getDate("date"),
                    resultSet.getInt("invoiceno")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error searching payments: " + e.getMessage());
        }
    }//GEN-LAST:event_jTextField5KeyReleased

