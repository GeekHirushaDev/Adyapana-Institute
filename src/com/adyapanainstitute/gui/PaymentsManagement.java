    private void loadPaymentsTable() {
        try {
            String query = "SELECT p.paymentid, s.name AS student, p.amount, p.date, i.invoiceno "
                    + "FROM payments p "
                    + "JOIN students s ON p.sno = s.sno "
                    + "JOIN invoices i ON p.invoiceno = i.invoiceno";
            ResultSet resultSet = MySQL.executeSearch(query);

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                model.addRow(new Object[]{
                    resultSet.getInt("paymentid"),
                    resultSet.getString("student"),
                    resultSet.getDouble("amount"),
                    resultSet.getDate("date"),
                    resultSet.getInt("invoiceno")
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading payments: " + e.getMessage());
        }
    }

    private void loadComboBoxes() {
        try {
            String studentQuery = "SELECT sno, name FROM students";
            ResultSet studentResult = MySQL.executeSearch(studentQuery);
            while (studentResult.next()) {
                jComboBox1.addItem(studentResult.getString("name"));
                jComboBox4.addItem(studentResult.getString("name"));
            }

            String teacherQuery = "SELECT tno, name FROM teachers";
            ResultSet teacherResult = MySQL.executeSearch(teacherQuery);
            while (teacherResult.next()) {
                jComboBox5.addItem(teacherResult.getString("name"));
            }

            String subjectQuery = "SELECT subno, subject FROM subjects";
            ResultSet subjectResult = MySQL.executeSearch(subjectQuery);
            while (subjectResult.next()) {
                jComboBox6.addItem(subjectResult.getString("subject"));
            }

            String invoiceQuery = "SELECT invoiceno FROM invoices";
            ResultSet invoiceResult = MySQL.executeSearch(invoiceQuery);
            while (invoiceResult.next()) {
                jComboBox2.addItem(invoiceResult.getString("invoiceno"));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading combo boxes: " + e.getMessage());
        }
    }

